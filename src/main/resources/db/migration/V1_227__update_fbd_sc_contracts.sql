CRS Finance Benefit & Debt services are available for those on probation who are subject to:

    - a Community Sentence or Suspended Sentence Order and are subject to a Rehabilitation Activity Requirement (RAR)
    - released on licence and post-sentence supervision.

CRS Finance Benefit & Debt services are available for those in prison who are.

    - Sentenced

Services will include a range of delivery methods including designing and delivery, support, advocacy and advice, and guidance and information. The level of the recipient’s complexity will determine the support required which includes:

    - Community: Low, Medium, and High complexity
    - Custody: High complexity only


For those on probation in the community, a specified number of RAR Activity Days will be allocated. Any Sessions delivered as part of a RAR Activity Day or as a mandated Licence or Post Sentence Supervision appointment is enforceable.

The CRS Finance Benefit & Debt service is designed to support People on Probation and in Prison to enable them to make progress towards agreed outcomes. These are identified in their individually tailored Action Plan and aims to secure one or more of the following outcomes:

    - access to appropriate financial products, advice and/or services.
    - to help maintain and sustain an income, safely manage money and
    reduce debt.
    - Develop or enhance financial management skills.
    - successfully navigate the benefits system, including online banking skills.
    - gains quick access to universal credit, where applicable.

